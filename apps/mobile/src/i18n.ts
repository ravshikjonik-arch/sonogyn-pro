/**
 * RU is the source language for the product.
 * In dev we warn if EN/ES contain keys that RU doesn't have.
 */
function warnMissingRuKeys() {
  if (!__DEV__) return;
  const ru = (i18n.translations.ru ?? {}) as Record<string, unknown>;
  const en = (i18n.translations.en ?? {}) as Record<string, unknown>;
  const es = (i18n.translations.es ?? {}) as Record<string, unknown>;
  const missFromEn = Object.keys(en).filter((k) => !(k in ru));
  const missFromEs = Object.keys(es).filter((k) => !(k in ru));
  const missing = [...new Set([...missFromEn, ...missFromEs])].sort();
  if (missing.length > 0) {
    console.warn(`[i18n] RU base is missing keys (${missing.length}): ${missing.join(", ")}`);
  }
}

warnMissingRuKeys();

export async function initI18n() {
  const savedLang = await AsyncStorage.getItem(APP_LANGUAGE_KEY);
  if (savedLang && isAppLanguage(savedLang)) {
    i18n.locale = savedLang;
    return;
  }
  i18n.locale = "ru";
}

export async function changeLanguage(lang: AppLanguage) {
  i18n.locale = lang;
  await AsyncStorage.setItem(APP_LANGUAGE_KEY, lang);
}

